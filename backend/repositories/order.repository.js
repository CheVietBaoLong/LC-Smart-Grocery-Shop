const prisma = require('../prisma/client');
const { BadRequestError } = require('../utils/errors');

async function findAll() {
  return prisma.orders.findMany({
    include: {
      order_item: { include: { product: true } },
      delivery_plan: true,
      order_shipping: { include: { address: true } },
    },
  });
}

async function findById(order_id) {
  return prisma.orders.findUnique({
    where: { order_id },
    include: {
      order_item: { include: { product: true } },
      delivery_plan: true,
      order_shipping: { include: { address: true } },
      payment_card: true,
    },
  });
}

async function findByCustomer(user_id) {
  return prisma.orders.findMany({
    where: { user_id },
    include: {
      order_item: { include: { product: true } },
      delivery_plan: true,
      order_shipping: { include: { address: true } },
    },
  });
}

// Creates delivery plan + order + order_items + order_shipping in one transaction.
// If a card_id is provided, the card is charged and balance is not touched.
// Otherwise, the account balance is checked and decremented.
async function create({ orderData, deliveryPlanData, items, address_id, total }) {
  return prisma.$transaction(async (tx) => {
    const payingByCard = !!orderData.card_id;

    if (!payingByCard) {
      const customer = await tx.customer.findUnique({ where: { user_id: orderData.user_id } });
      if (!customer || Number(customer.balance) < total) {
        throw new BadRequestError('Insufficient balance');
      }
      await tx.customer.update({
        where: { user_id: orderData.user_id },
        data: { balance: { decrement: total } },
      });
    }

    // Check and deduct stock for each item across warehouses
    for (const item of items) {
      const stocks = await tx.stock.findMany({
        where: { product_id: item.product_id },
        orderBy: { quantity: 'desc' },
      });

      const totalAvailable = stocks.reduce((sum, s) => sum + (s.quantity ?? 0), 0);
      if (totalAvailable < item.quantity) {
        throw new BadRequestError(`Insufficient stock for product ${item.product_id}`);
      }

      let remaining = item.quantity;
      for (const s of stocks) {
        if (remaining <= 0) break;
        const deduct = Math.min(remaining, s.quantity ?? 0);
        await tx.stock.update({
          where: { product_id_warehouse_id: { product_id: item.product_id, warehouse_id: s.warehouse_id } },
          data: { quantity: { decrement: deduct } },
        });
        remaining -= deduct;
      }
    }

    // Create the delivery plan for this order and link it.
    // Use raw SQL to avoid the PrismaPg driver adapter passing explicit NULL for
    // the GENERATED BY DEFAULT AS IDENTITY primary key.
    let delivery_id = orderData.delivery_id ?? null;
    if (deliveryPlanData) {
      const [plan] = await tx.$queryRaw`
        INSERT INTO delivery_plan (delivery_type, delivery_cost, ship_date, delivery_date)
        VALUES (${deliveryPlanData.delivery_type}, ${deliveryPlanData.delivery_cost},
                ${deliveryPlanData.ship_date}, ${deliveryPlanData.delivery_date})
        RETURNING delivery_id
      `;
      delivery_id = plan.delivery_id;
    }

    const order = await tx.orders.create({
      data: { ...orderData, delivery_id },
    });

    await tx.order_item.createMany({
      data: items.map((item) => ({ ...item, order_id: order.order_id })),
    });

    if (address_id) {
      await tx.order_shipping.create({
        data: { order_id: order.order_id, address_id },
      });
    }

    return order;
  });
}

async function updateStatus(order_id, status) {
  return prisma.orders.update({ where: { order_id }, data: { status } });
}

async function cancelAndRefund(order_id, user_id, refundAmount) {
  return prisma.$transaction(async (tx) => {
    const order = await tx.orders.update({
      where: { order_id },
      data: { status: 'Cancelled' },
    });
    // Only refund to balance if the order was paid by balance (no card)
    if (!order.card_id) {
      await tx.customer.update({
        where: { user_id },
        data: { balance: { increment: refundAmount } },
      });
    }
    return order;
  });
}

async function remove(order_id) {
  return prisma.orders.delete({ where: { order_id } });
}

module.exports = { findAll, findById, findByCustomer, create, updateStatus, cancelAndRefund, remove };